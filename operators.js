//Operators

//Flow of operators
//Variables------------------>Expressions--------------->Algorithms


let x= 20;
let y= 10;

console.log(x+y);
console.log(x-y);
console.log(x*y);
console.log(x/y);
console.log(x%y);
console.log(x**y);

//Incremental Operators(++)
console.log(x++);
console.log(x);

console.log(y++);
//Decremental Operators(--)
console.log(--x);
console.log(x);
console.log(--y);
console.log(y);
